void sensor_init_apollo(void)
{
	XM_U16 u16Num,u16i;
	u16Num = sizeof(gau16SnsInit_apollo)/sizeof(gau16SnsInit_apollo[0]);
	sensor_write_register((XM_U32)gau16SnsInit_apollo[0][0], (XM_U32)gau16SnsInit_apollo[0][1]);	// reset all registers
	SysDelay_ms(10);
	for(u16i=1; u16i < u16Num; u16i++)
	{
		sensor_write_register((XM_U32)gau16SnsInit_apollo[u16i][0], (XM_U32)gau16SnsInit_apollo[u16i][1]);
	}
	DEBUG("------------- apollo 4K 15fps init ok! (@20181221)----------------\n");
}


