void sensor_init_sc5239(void)
{
	XM_U16 u16Num,u16i;
	u16Num = sizeof(gau16SnsInit_sc5239)/sizeof(gau16SnsInit_sc5239[0]);
	sensor_write_register((XM_U32)gau16SnsInit_sc5239[0][0], (XM_U32)gau16SnsInit_sc5239[0][1]);	// reset all registers
	SysDelay_ms(50);
	for(u16i=1; u16i < u16Num; u16i++)
	{
		sensor_write_register((XM_U32)gau16SnsInit_sc5239[u16i][0], (XM_U32)gau16SnsInit_sc5239[u16i][1]);
	}
	DEBUG("------------- SC5239 4M 15fps  init ok! (@201801225_158x4)----------------\n");
}


