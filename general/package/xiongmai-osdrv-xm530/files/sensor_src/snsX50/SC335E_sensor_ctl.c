void sensor_init_sc335e(void)
{
	XM_U16 u16Num,u16i;
	u16Num = sizeof(gau16SnsInit_sc335e)/sizeof(gau16SnsInit_sc335e[0]);
	sensor_write_register((XM_U32)gau16SnsInit_sc335e[0][0], (XM_U32)gau16SnsInit_sc335e[0][1]);	// reset all registers
	SysDelay_ms(50);
	for(u16i=1; u16i < u16Num; u16i++)
	{
		sensor_write_register((XM_U32)gau16SnsInit_sc335e[u16i][0], (XM_U32)gau16SnsInit_sc335e[u16i][1]);
	}
	SysDelay_ms(20);
	DEBUG("------------- SC5300 5M 15fps  init ok! (@201801225_158x4)----------------\n");
}


